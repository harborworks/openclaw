export function randomAgentName(exclude: string[] = []): string {
  const available = AGENT_NAMES.filter((n) => !exclude.includes(n));
  if (available.length === 0) return "Agent-" + Math.floor(Math.random() * 9999);
  return available[Math.floor(Math.random() * available.length)];
}
